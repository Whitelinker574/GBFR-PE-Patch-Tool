var masterySpecializationRoots = func() map[uint32]struct{} {
	result := make(map[uint32]struct{}, 174)
	for _, value := range strings.Fields(masterySpecializationRootHexes) {
		parsed, err := strconv.ParseUint(value, 16, 32)
		if err != nil {
			panic("invalid audited mastery specialization root: " + value)
		}
		result[uint32(parsed)] = struct{}{}
	}
	return result
}()

func isMasterySpecializationHash(hash uint32) bool {
	_, ok := masterySpecializationRoots[hash]
	return ok
}
