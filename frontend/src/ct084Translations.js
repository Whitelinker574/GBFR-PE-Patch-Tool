const featureTextReplacements = Object.freeze(Object.fromEntries(
  Object.keys(zhFeatureNames).flatMap((id) => {
    const zh = zhFeatureNames[id]
    const en = ct084EnglishFeatureNames[id]
    const trimmedZH = zh.replace(/[：:]\s*$/u, '')
    const trimmedEN = en.replace(/:\s*$/u, '')
    return trimmedZH === zh ? [[zh, en]] : [[zh, en], [trimmedZH, trimmedEN]]
  }),
))

const englishReplacements = Object.freeze(Object.entries({
  ...featureTextReplacements,
  ...groupEnglishNames,
  ...textEnglish,
}).sort(([left], [right]) => right.length - left.length))

export function translateCT084FeatureName(feature, locale = 'zh') {
  const source = String(feature?.displayName || feature?.name || '').replace(/[：:]\s*$/u, '')
  if (locale !== 'en') return source
  return String(ct084EnglishFeatureNames[String(feature?.id || '')] || source).replace(/:\s*$/u, '')
}

export function translateCT084GroupName(group, locale = 'zh') {
  const source = String(group ?? '')
  return locale === 'en' ? groupEnglishNames[source] || source : source
}

export function translateCT084Text(value, locale = 'zh') {
  let output = String(value ?? '')
  if (locale !== 'en') return output
  for (const [source, translated] of englishReplacements) output = output.split(source).join(translated)
  return output
}
